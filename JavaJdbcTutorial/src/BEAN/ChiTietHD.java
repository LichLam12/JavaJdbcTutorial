package BEAN;

public class ChiTietHD {
	private int soChiTiet;
	private int soHD;
	private int maMyPham;
	private int soLuong;
	private boolean hoaDonDo;
	public int getSoChiTiet() {
		return soChiTiet;
	}
	public void setSoChiTiet(int soChiTiet) {
		this.soChiTiet = soChiTiet;
	}
	public int getSoHD() {
		return soHD;
	}
	public void setSoHD(int soHD) {
		this.soHD = soHD;
	}
	public int getMaMyPham() {
		return maMyPham;
	}
	public void setMaMyPham(int maMyPham) {
		this.maMyPham = maMyPham;
	}
	public int getSoLuong() {
		return soLuong;
	}
	public void setSoLuong(int soLuong) {
		this.soLuong = soLuong;
	}
	public boolean isHoaDonDo() {
		return hoaDonDo;
	}
	public void setHoaDonDo(boolean hoaDonDo) {
		this.hoaDonDo = hoaDonDo;
	}
	
	
}
