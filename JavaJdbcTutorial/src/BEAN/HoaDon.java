package BEAN;

import java.sql.Date;

public class HoaDon {
	private int soHD;
	private Date ngayXuatHD;
	private double triGiaHD;
	private String tinhTrangHD;
	private String maKH;
	private String maNV;
	public int getSoHD() {
		return soHD;
	}
	public void setSoHD(int soHD) {
		this.soHD = soHD;
	}
	public Date getNgayXuatHD() {
		return ngayXuatHD;
	}
	public void setNgayXuatHD(Date ngayXuatHD) {
		this.ngayXuatHD = ngayXuatHD;
	}
	public double getTriGiaHD() {
		return triGiaHD;
	}
	public void setTriGiaHD(double triGiaHD) {
		this.triGiaHD = triGiaHD;
	}
	public String getTinhTrangHD() {
		return tinhTrangHD;
	}
	public void setTinhTrangHD(String tinhTrangHD) {
		this.tinhTrangHD = tinhTrangHD;
	}
	public String getMaKH() {
		return maKH;
	}
	public void setMaKH(String maKH) {
		this.maKH = maKH;
	}
	public String getMaNV() {
		return maNV;
	}
	public void setMaNV(String maNV) {
		this.maNV = maNV;
	}
	
	
	
	
}
